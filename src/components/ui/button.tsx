import * as React from "react";
import { Slot } from "@radix-ui/react-slot";
import { cva, type VariantProps } from "class-variance-authority";

import { cn } from "@/lib/utils";

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap text-sm font-medium ring-offset-background transition-all duration-300 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0 font-body tracking-wide",
  {
    variants: {
      variant: {
        default: "bg-primary text-primary-foreground hover:bg-primary/90",
        destructive: "bg-destructive text-destructive-foreground hover:bg-destructive/90",
        outline: "border border-border bg-transparent text-foreground hover:border-primary hover:text-primary",
        secondary: "bg-secondary text-secondary-foreground hover:bg-secondary/80",
        ghost: "hover:bg-accent/10 hover:text-accent",
        link: "text-primary underline-offset-4 hover:underline",
        // Luxury variants
        luxury: "bg-gradient-gold text-primary-foreground font-semibold uppercase tracking-widest hover:shadow-gold transition-all duration-500",
        "luxury-outline": "border border-primary bg-transparent text-primary uppercase tracking-widest hover:bg-primary hover:text-primary-foreground transition-all duration-500",
        "luxury-ghost": "bg-transparent text-primary uppercase tracking-widest hover:bg-primary/10 transition-all duration-300",
        hero: "bg-transparent border border-foreground/20 text-foreground uppercase tracking-[0.3em] hover:border-primary hover:text-primary transition-all duration-500",
      },
      size: {
        default: "h-10 px-4 py-2 rounded-sm",
        sm: "h-9 px-3 rounded-sm text-xs",
        lg: "h-12 px-8 rounded-sm",
        xl: "h-14 px-12 rounded-sm text-base",
        icon: "h-10 w-10 rounded-sm",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  },
);

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean;
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button";
    return <Comp className={cn(buttonVariants({ variant, size, className }))} ref={ref} {...props} />;
  },
);
Button.displayName = "Button";

export { Button, buttonVariants };
